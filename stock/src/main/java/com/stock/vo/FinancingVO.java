package com.stock.vo;

public class FinancingVO {
	private String transactionDate;
	private String marginAmount;
	private String marginBalance;
	private String shortAmount;
	private String shortBalance;
	
	public String getTransactionDate() {
		return transactionDate;
	}
	public void setTransactionDate(String transactionDate) {
		this.transactionDate = transactionDate;
	}
	public String getMarginAmount() {
		return marginAmount;
	}
	public void setMarginAmount(String marginAmount) {
		this.marginAmount = marginAmount;
	}
	public String getMarginBalance() {
		return marginBalance;
	}
	public void setMarginBalance(String marginBalance) {
		this.marginBalance = marginBalance;
	}
	public String getShortAmount() {
		return shortAmount;
	}
	public void setShortAmount(String shortAmount) {
		this.shortAmount = shortAmount;
	}
	public String getShortBalance() {
		return shortBalance;
	}
	public void setShortBalance(String shortBalance) {
		this.shortBalance = shortBalance;
	}
	
	
}
